	public BufferedImage loadImage(String path) throws IOException{
		try{
			File image = new File(path);
			BufferedImage bi = ImageIO.read(image);
			return bi;
		}
		catch(Exception ex){
			ex.printStackTrace();
			throw new IOException();
		}
	}

